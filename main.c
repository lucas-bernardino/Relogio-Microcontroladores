void display_hand_clock ()
{
    if (flag_hands)
    {
        TimerIntDisable(TIMER0_BASE, TIMER_TIMA_TIMEOUT);
        uint32_t timer_cont = 0;

        Nokia5110_Clear();
        Nokia5110_ClearBuffer();

        handle_hands(copia_0);

        for (timer_cont = 0; timer_cont < 1000000; timer_cont ++)
        {
            continue;
        }

        Nokia5110_Clear();
        Nokia5110_ClearBuffer();

        handle_hands(copia_1);

        for (timer_cont = 0; timer_cont < 1000000; timer_cont ++)
        {
            continue;
        }

        Nokia5110_Clear();
        Nokia5110_ClearBuffer();

        for (timer_cont = 0; timer_cont < 2000000; timer_cont ++)
        {
            continue;
        }

        handle_hands(copia_2);

        for (timer_cont = 0; timer_cont < 1000000; timer_cont ++)
        {
            continue;
        }

        Nokia5110_Clear();
        Nokia5110_ClearBuffer();

        handle_hands(copia_3);

        for (timer_cont = 0; timer_cont < 1000000; timer_cont ++)
        {
            continue;
        }

        Nokia5110_Clear();
        Nokia5110_ClearBuffer();

        for (timer_cont = 0; timer_cont < 2000000; timer_cont ++)
        {
            continue;
        }

        handle_hands(copia_4);

        for (timer_cont = 0; timer_cont < 1000000; timer_cont ++)
        {
            continue;
        }

        Nokia5110_Clear();
        Nokia5110_ClearBuffer();

        handle_hands(copia_5);

        for (timer_cont = 0; timer_cont < 1000000; timer_cont ++)
        {
            continue;
        }

        Nokia5110_Clear();
        Nokia5110_ClearBuffer();

        for (timer_cont = 0; timer_cont < 2000000; timer_cont ++)
        {
            continue;
        }
        flag_hands = false;

        TimerIntClear(TIMER0_BASE, TIMER_TIMA_TIMEOUT);
        TimerLoadSet(TIMER0_BASE, TIMER_A, SysCtlClockGet() * 10);
        IntEnable(INT_TIMER0A);
        TimerIntEnable(TIMER0_BASE, TIMER_TIMA_TIMEOUT);
        TimerEnable(TIMER0_BASE, TIMER_A);
    }
}

